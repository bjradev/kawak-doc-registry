CREATE OR REPLACE VIEW VW_DOC_DOCUMENTO AS
SELECT
  d.DOC_ID,
  d.DOC_NOMBRE,
  CONCAT(t.TIP_PREFIJO,'-',p.PRO_PREFIJO,'-',d.DOC_CODIGO) AS CODIGO,
  d.DOC_CODIGO,
  d.DOC_CONTENIDO,
  d.DOC_ID_TIPO,
  d.DOC_ID_PROCESO
FROM DOC_DOCUMENTO d
JOIN TIP_TIPO_DOC t ON t.TIP_ID = d.DOC_ID_TIPO
JOIN PRO_PROCESO  p ON p.PRO_ID = d.DOC_ID_PROCESO;
